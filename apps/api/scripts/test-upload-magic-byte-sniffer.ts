// ── MagicByteSniffer Transform tests ───────────────────────────────────

/**
 * Consume a Transform pipeline to completion using an explicit Writable
 * so `_flush` fires reliably on all intermediate transforms.
 */
async function sniff(input: Uint8Array): Promise<{ mime: string | undefined; chunks: Buffer[] }> {
  let mime: string | undefined;
  const chunks: Buffer[] = [];
  const sniffer = new MagicByteSniffer(4 * 1024, async (detectedMime) => { mime = detectedMime; });

  return new Promise<void>((resolve, reject) => {
    const dest = new Writable({
      write(chunk: Buffer, _encoding, cb) { chunks.push(chunk); cb(); },
      final(cb) { cb(); resolve(); },
    }).on('error', reject);

    Readable.from(Buffer.from(input))
      .pipe(sniffer)
      .pipe(dest)
      .on('error', reject);
  }).then(() => ({ mime, chunks: chunks as Buffer[] }));
}

test('MagicByteSniffer: onResult fires with correct MIME for each file type', async () => {
  assert.equal((await sniff(VALID_PNG)).mime, 'image/png');
  assert.equal((await sniff(VALID_JPEG)).mime, 'image/jpeg');
  assert.equal((await sniff(VALID_ZIP)).mime, 'application/zip');
  assert.equal((await sniff(VALID_GIF)).mime, 'image/gif');
  assert.equal((await sniff(UNKNOWN_BYTES)).mime, undefined);
  assert.equal((await sniff(EMPTY_BYTES)).mime, undefined);
});

test('MagicByteSniffer: forwards all bytes downstream unchanged', async () => {
  const { chunks } = await sniff(VALID_PNG);
  const received = Buffer.concat(chunks);
  assert.ok(Buffer.isBuffer(received), 'Should receive Buffer chunks');
  assert.ok(received.equals(Buffer.from(VALID_PNG)), 'Received bytes must match input exactly');
});

test('MagicByteSniffer: works when input is smaller than sniffBytes', async () => {
  // 24 bytes into PNG (past magic + IHDR chunk header) — file-type needs
  // enough to reach the IHDR chunk header. Verifies _flush fires and
  // sniff fires even when input < sniffBytes.
  const shortInput = new Uint8Array(VALID_PNG.buffer, VALID_PNG.byteOffset, 24);
  const { mime } = await sniff(shortInput);
  assert.equal(mime, 'image/png', '24-byte PNG prefix should be detected as image/png');
});

test('MagicByteSniffer: onResult called exactly once per pipeline', async () => {
  let callCount = 0;
  const sniffer = new MagicByteSniffer(4 * 1024, async () => { callCount += 1; });
  const dest = new Writable({ write(_chunk, _enc, cb) { cb(); }, final(cb) { cb(); } });
  await new Promise<void>((resolve, reject) => {
    Readable.from(Buffer.from(VALID_JPEG)).pipe(sniffer).pipe(dest).on('finish', resolve).on('error', reject);
  });
  assert.equal(callCount, 1, `onResult must be called exactly once, got ${callCount}`);
});